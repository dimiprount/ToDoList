package com.dimiprount.todolist;

public class Notepad {
	int id;
	String note;

	public Notepad() {

	}

	public int getID() {
		return this.id;
	}

	public void setID(int keyId) {
		this.id = keyId;
	}

	public String getNote() {
		return this.note;
	}

	public void setNote(String note) {
		this.note = note;
	}

}
