package com.dimiprount.todolist;

public class Password {
	String pass;

	// Empty constructor
	public Password() {

	}

	// constructor
	public Password(String password) {
		this.pass = password;
	}

	// getting name
	public String getPass() {
		return this.pass;
	}

	// setting name
	public void setPass(String password) {
		this.pass = password;
	}
}
